package es.uma.khaos.docking_service.model;

public class Macro {
	
	
	public int id;
	public String macro;
	
	
	public Macro(int id, String macro) {
		this.id = id;
		this.macro = macro;
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getMacro() {
		return macro;
	}


	public void setMacro(String macro) {
		this.macro = macro;
	}
	
	
	
	
	


}
